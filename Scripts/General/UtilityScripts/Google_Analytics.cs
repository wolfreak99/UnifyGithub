namespace UnifyGithub.General.UtilityScripts
{
This is a Boo script that allows use of Google Analytics for event/stat tracking in Unity3D. The script can be used in native and web player builds alike and will log events to your Google Analytics account. 
The script is available on Github. To use it, save it to your project folder, attach it to any GameObject and use its methods to fire events at Google. 
}
