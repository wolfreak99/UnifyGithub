namespace UnifyGithub.Editor.EditorScripts
{
    In early, but working stage (with some limitations). Put this script in you project/Assets/Editor folder. Reserves keyboard shortcut Ctrl-H (win) or Cmd-H (Mac). 
    This is what it does: 
    Makes list in window of game objects that have Mesh Renderer and adds toggle for user to turn it on and off. Basicly it just enables/disables game objects Mesh Renderer. 
     
     
