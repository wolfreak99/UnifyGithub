Search Objects based on Layer in Unity3D This is an editor script for Unity3D. This tool will help you, if you are working on a large scale project & you often need to search for objects with specific layer. Unity3D searches objects based on Name & Type. This helps you search for objects based on Layer. 
How To Use: 
   Press CTRL+SHIFT+L to launch the tool on PC. On MAC use CMD+SHIFT+L.
   You can also find it in MyTools->Layer Search.
   Choose Layer name from the drop down list.
   The objects with the selcted layer are listed.
   Click on the object, it will frame the object.
You can download the files from here. 
Hope it helps someone. 
