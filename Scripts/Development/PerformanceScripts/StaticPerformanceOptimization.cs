namespace UnifyGithub.Development.PerformanceScripts
{
Author: Joachim Ante 
DescriptionThis script optimizes performance on old cards by detecting the graphics card and dropping quality levels automatically. 
UsageDownload the package and attach the script to an empty game object in your scene. The script will set the quality setting on slow graphics cards. It will also adjust the Terrain settings on old graphics cards and disable glow if a reference to a glow effect has been assigned. 
