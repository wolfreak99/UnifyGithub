namespace UnifyGithub.GUI.Unity20GUIScripts
{
This is a set of extensions for Unity 2.6+ that expand upon the GUI, GUILayout, EditorGUI, and EditorGUILayout. These include: 
Automatically select all text when a textarea gets focus: 
s = GUILayoutExt.AutoSelectTextArea("myWidgetName", s);(All signatures for GUI.TextArea, GUILayout.TextArea, EditorGUI.TextArea, and EditorGUILayout.TextArea work, with the addition of a unique "widget name" parameter.) 
