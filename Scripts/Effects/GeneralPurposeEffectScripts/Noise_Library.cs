Ported LibNoise [1] noise library from LibNoise.Xna [2]to Unity. Like those two, it is released under the LGPL. Generates Perlin, RiggedMultifractal,Voronoi and Billow noises and has Operators for combining them. Noise2D is a convenience class for creating Texture2D from noises. 
Available from the Showcase forum http://forum.unity3d.com/threads/68764-LibNoise-Ported-to-Unity?p=439185#post439185 [3] 
